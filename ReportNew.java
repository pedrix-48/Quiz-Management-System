    public void Imprimi() {
        int screenNumber = 1;
        int x = 10;
        int y = 5;
        int width = 595;
        int heigth = 365;
        Rectangle screenBounds = GraphicsEnvironment.getLocalGraphicsEnvironment().getScreenDevices()[screenNumber - 1].getDefaultConfiguration().getBounds();
        BufferedImage screenCapture = new BufferedImage(width, heigth, BufferedImage.TYPE_INT_RGB);

        try {
            Robot robot = new Robot();
            robot.delay(100);
            robot.createScreenCapture(new Rectangle(x, y, width, heigth)).copyData(screenCapture.getRaster());
        } catch (AWTException e) {
            JOptionPane.showMessageDialog(null, "IMPRIMI FALLA", "ATENSAUN", JOptionPane.ERROR_MESSAGE);
        }

        try {
            ImageIO.write(screenCapture, "png", new File(jTextField2.getText() + "_VALOR_QUIZ.png"));
            JOptionPane.showMessageDialog(null, "DOWNLOAD SUSESU!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "IMPRIMI FALLA", "ATENSAUN", JOptionPane.ERROR_MESSAGE);
        }
    }

