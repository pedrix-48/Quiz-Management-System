    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            int a = JOptionPane.showConfirmDialog(jf, "HAKARAK APAGA DADOS?", "KOMFIRMASAUN", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("delete from pergunta");
                ps.executeUpdate();
                JFrame jf1 = new JFrame();
                jf1.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf1, "APAGA DADOS SUSESU!!");
                setVisible(false);
                new PerguntaSira().setVisible(true);
            }
        } catch (HeadlessException | SecurityException | SQLException e) {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

